package package2;

public class Expression {
	public int Add(int a, int b)
	{
		int c=(a+b);
		return c;
	}
	public int Sub(int a, int b)
	{
		int c=(a-b);
		return c;
	}
	public int Mul(int a, int b)
	{
		int c=(a*b);
		return c;
	}
	public int Div(int a, int b)
	{
		int c=(a/b);
		return c;
	}
}
